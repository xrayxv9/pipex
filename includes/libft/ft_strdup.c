#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		len;
	int			i;
	char		*dup;

	i = 0;
	len = ft_strlen(s);
	dup = malloc((1 + len) * sizeof(char));
	if (!dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
